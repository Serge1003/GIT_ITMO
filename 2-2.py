lst = list(input('Введите массив чисел в  одну строку: '))  # ввод по маске: 123456...n
l_min = lst[0]; i = 1; n = len(lst) #назначили локальный минимум
while i < n:                                #определили длину списка
    if lst[i] <  l_min: l_min = lst[i]
    i += 1
print("Минимальное значение:", l_min)
